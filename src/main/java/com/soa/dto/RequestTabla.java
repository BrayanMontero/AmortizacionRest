package com.soa.dto;

/**
 * 
 */
public class RequestTabla {
    private String num_tarjeta;
    private Integer interes;
    private Integer meses;
    private Double cantida;
    /**
     * @return the num_tarjeta
     */
    public String getNum_tarjeta() {
        return num_tarjeta;
    }
    /**
     * @param num_tarjeta the num_tarjeta to set
     */
    public void setNum_tarjeta(String num_tarjeta) {
        this.num_tarjeta = num_tarjeta;
    }
    /**
     * @return the interes
     */
    public Integer getInteres() {
        return interes;
    }
    /**
     * @param interes the interes to set
     */
    public void setInteres(Integer interes) {
        this.interes = interes;
    }
    /**
     * @return the meses
     */
    public Integer getMeses() {
        return meses;
    }
    /**
     * @param meses the meses to set
     */
    public void setMeses(Integer meses) {
        this.meses = meses;
    }
    /**
     * @return the cantida
     */
    public Double getCantida() {
        return cantida;
    }
    /**
     * @param cantida the cantida to set
     */
    public void setCantida(Double cantida) {
        this.cantida = cantida;
    }

}
